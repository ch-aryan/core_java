package com.javabasics;

public class JavaBreak {
    public static void main(String[] args) {
        for(int i = 0; i<10; i++) {

                System.out.println("i = " + i);

                if (i == 5) {
                    break;
                }
            }
        int i =0;

        while(i<10){
            System.out.println(i);
            i++;
            if(i == 5){
                break;
            }
        }
        }
    }

